CREATE TABLE IF NOT EXISTS VOTANTES(
    ID_VOTANTE INT NOT NULL AUTO_INCREMENT PRIMARY KEY,
    NOMBRE VARCHAR(32) NOT NULL,
    NUM_INE VARCHAR(13) NOT NULL,
    GENERO CHAR(1) NOT NULL,
    UNIQUE (NUM_INE),
    CHECK (GENERO = 'H' OR GENERO = 'M')
);

CREATE TABLE IF NOT EXISTS VOTOS(
    ID_VOTO INT NOT NULL AUTO_INCREMENT PRIMARY KEY,
    ID_VOTANTE INT NOT NULL,
    PARTIDO VARCHAR(16) NOT NULL,
    FECHA DATE NOT NULL,
    FOREIGN KEY (ID_VOTANTE) REFERENCES VOTANTES(ID_VOTANTE)
);

